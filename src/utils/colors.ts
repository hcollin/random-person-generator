import { arnd } from "./randUtils";

export interface Color {
    id: string;
    name: string;
    rgb: string;
    red: number;
    green: number;
    blue: number;
}

export function getRandomColor(): Color {
    return arnd(COLORS);
}

export function findColors(searchString: string | string[], asAnd = false): Color[] {
    const skeys: string[] = (typeof searchString === "string" ? [searchString] : searchString).map((s: string) =>
        s.toLowerCase()
    );

    const results: Color[] = COLORS.filter((c: Color) => {
        let isMatch = asAnd;
        for (let i = 0; i < skeys.length; i++) {
            const k = skeys[i];

            const included = c.id.includes(k);

            if (!asAnd && included) {
                return true;
            }
            if (asAnd && !included) {
                isMatch = false;
            }
        }
        return isMatch;
    });

    return results;
    1;
}

